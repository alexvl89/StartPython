# Ввод числа с клавиатуры
number = int(input("Введите число: "))

# Десятичное представление
print("Десятичное представление:", number)

# Двоичное представление
binary_representation = bin(number)
print("Двоичное представление:", binary_representation)

# Восьмеричное представление
octal_representation = oct(number)
print("Восьмеричное представление:", octal_representation)

# Шестнадцатеричное представление
hexadecimal_representation = hex(number)
print("Шестнадцатеричное представление:", hexadecimal_representation)
